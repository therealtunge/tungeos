void write_chr_helper(char chr, int x, int y) {
	for (int i = 0; i < 3; i++) {
		for (int j = 0; j < 5; j++) {
			write_pixel8(i + x, j + y, font[chr][i + (j * 3)]);
		}
		
	}
}

void write_chr(char chr) {
	serial_printf("%d ", chr);
	if (chr == '\n') {
			vga_y += 6;
			vga_x = 2;
			return;
	}
	if (chr > 31) {
		write_chr_helper(chr - 32, vga_x, vga_y);
		vga_x += 4;
	}
}