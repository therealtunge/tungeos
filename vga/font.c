void _write_chr(char chr, int x, int y) {
	for (int i = 0; i < 3; i++) {
		for (int j = 0; j < 5; j++) {
			write_pixel8(i + x, j + y, font[chr - 32][i + (j * 3)]);
		}
		
	}
}

void write_chr(char chr) {
	if (chr == '\n') {
			vga_y += 6;
			vga_x = 2;
			return;
	}
	if (chr > 31) {
		_write_chr(chr, vga_x, vga_y);
		vga_x += 4;
	} else if (chr == 8) {
		if (vga_x == 2) {
			vga_y -= 5;
		}
		vga_x -= 4;
		_write_chr(' ', vga_x, vga_y);
	}
}